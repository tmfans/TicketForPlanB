package com.example.xlq_tm.planbfortickets.DataBean;

public class StationsBean {


    /**
     * stationCode : VAP
     * stationName : 北京北
     * stationSpell : beijingbei
     */

    private String stationCode;
    private String stationName;
    private String stationSpell;

    public String getStationCode() {
        return stationCode;
    }

    public void setStationCode(String stationCode) {
        this.stationCode = stationCode;
    }

    public String getStationName() {
        return stationName;
    }

    public void setStationName(String stationName) {
        this.stationName = stationName;
    }

    public String getStationSpell() {
        return stationSpell;
    }

    public void setStationSpell(String stationSpell) {
        this.stationSpell = stationSpell;
    }
}
